public class OperatoryMatematyczne {
    public static void main(String[] args) {
        int firstNumber = 4;
        int secondNumber = 6;
        int dodawanie = secondNumber + firstNumber;
        int odejmowanie = secondNumber - firstNumber;
        int mnozenie = secondNumber * firstNumber;
        int dzielenie =  secondNumber / firstNumber;
        int modulo = secondNumber % firstNumber; // reszta z dzielenia
        System.out.println(dodawanie + odejmowanie + mnozenie + dzielenie + modulo);
    }


}
